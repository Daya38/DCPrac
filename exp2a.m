N = 128;
x1 = ones(1, 4);
x2 = ones(1, 6);
n1 = length(x1);
n2 = length(x2);
x1 = [x1, zeros(1, N - n1)];
x2 = [x2, zeros(1, N - n2)];
n = 0 : N - 1;
k = 0 : N - 1;
w = 2 * pi * k / N;
X1 = fft(x1);
X2 = fft(x2);
n0 = 2;
y = [zeros(1, n0), x1(1 : N - n0)];
Y = fft(y);
Y1 = X1 .* exp(4j * w * n0);
figure;
subplot(311);
plot(-10 : N, [zeros(1, 11), x1], 'LineWidth', 1);
hold on;
plot(-10 : N, [zeros(1, 11), y], 'LineWidth', 1);
xlim([-10 18]);
ylim([-0.2 1.2]);
xlabel('n');
ylabel('Magnitude');
title('x1(n) & x1(n - n0)');
subplot(312);
plot(w, abs(Y), '--', 'LineWidth', 1);
xlim([0 2 * pi]);
xlabel('w');
ylabel('Magnitude');
title('DFT [x1(n - n0)]');
subplot(313);
stem(w, abs(Y1), 'filled');
xlim([0 2 * pi]);
xlabel('w');
ylabel('Magnitude');
title('DFT [x1 * exp(-j*w*n0)]');